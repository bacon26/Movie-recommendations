# Recommender systems använder sig oftast av ena eller båda av dessa tekniker:
# - Collaborative filtering (CF) [Liknande användare]
# - Content based filtering (CBF) [Liknande produkt-egenskaper]
# CF bygger en model utifrån användarens tidigare beteende samt beteende
# hos liknande användare. Rekommenderar produkter som liknande användare har gillat.
# CBF använder fördefinierade egenskaper och rekommenderar produkter med liknande
# egenskaper som användaren tidigare gillat.
